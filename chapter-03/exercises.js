////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  //set bCount to 0
  let charCount = 0;
  //loop to iterate through string
  for(let i = 0; i < string.length; i++){
    //if string at index i is roughly equal to char
    if(string[i] == char){
      //icremenet charCount by 1 if true
      charCount++;
    }

  }
  //return bCount
  return charCount;
}

