////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  //if condition to check if num is positive or neg
  if(num % 2 === 0){
    return true;
    //returns false if num is odd
  } else if(num % 2 == 1){
    return false;
    //returns false if num not positive
  } else {
    return false;
  }
    
    
}



